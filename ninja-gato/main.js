$('#imagen0').click(function() {
  
  if($("#imagen0").attr("src")=="img/ninja0.png"){
    $("#imagen0").attr("src","img/cat0.png");
  }
  else{
    $("#imagen0").attr("src","img/ninja0.png");
  }
});

